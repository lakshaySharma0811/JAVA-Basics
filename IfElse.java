public class IfElse {

  public static void main(String[] args) {

    int age = 20;

    if (age == 10) {
      System.out.println("I am inside if block");
    } else if (age < 18) {
      System.out.println("I am inside else if block");
    } else {
      System.out.println("I am inside else block");
    }
  }
}
