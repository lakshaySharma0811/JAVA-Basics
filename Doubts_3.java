public class Doubts_3 {
  public static void main(String[] args) {
    int n = 4;

    for (int i = 10; i >= 7; i--) {
      for (int j = 1; j <= n; j++) {
        System.out.print("1 ");
      }
      for (int j = 1; j <= n; j++) {
        System.out.print("2 ");
      }
      for (int j = 1; j <= n; j++) {
        System.out.print("3 ");
      }
      System.out.println();
    }
  }
}

